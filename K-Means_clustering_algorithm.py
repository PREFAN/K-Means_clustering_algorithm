plt.scatter(dataset2[:, 0], dataset2[:, 1], c=labels2_1, cmap='viridis', alpha=0.5)
plt.scatter(centroids2_1[:, 0], centroids2_1[:, 1], c='red', marker='x', s=200, label='Centroids')
plt.title('Dataset 2 - Init 1 (Cluster Centers)')
plt.legend()

